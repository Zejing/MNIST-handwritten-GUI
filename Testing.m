%function test_example_CNN
isOctave=0;
addpath(genpath('../data/'));
addpath(genpath('../CNN/'));
addpath(genpath('../Util/'));
load mnist_uint8;

train_x = double(reshape(train_x',28,28,60000))/255;
test_x = double(reshape(test_x',28,28,10000))/255;
train_y = double(train_y');
test_y = double(test_y');

%% ex1 Train a 6c-2s-12c-2s Convolutional neural network 
%will run 1 epoch in about 200 second and get around 11% error. 
%With 100 epochs you'll get around 1.2% error

rand('state',0)

cnn.layers = {
    struct('type', 'i') %input layer
    struct('type', 'c', 'outputmaps', 6, 'kernelsize', 5) %convolution layer
    struct('type', 's', 'scale', 2) %sub sampling layer
    struct('type', 'c', 'outputmaps', 12, 'kernelsize', 5) %convolution layer
    struct('type', 's', 'scale', 2) %subsampling layer
};

ep = str2double(get(handles.edit3, 'String')); %number of epoch
lr = str2double(get(handles.edit4, 'String')); %adjust learning rate
ba = str2double(get(handles.edit5, 'String')); %adjust batchsize

opts.alpha = lr;% Learning rate
opts.batchsize = ba;
opts.numepochs = ep;

cnn = cnnsetup(cnn, train_x, train_y);
cnn = cnntrain(cnn, train_x, train_y, opts);

[er, bad] = cnntest(cnn, test_x, test_y);

%mo = str2strget(handles.edit6, 'String');%Type the model name

save 'cnnmodel_1' cnn; % save the training model
%plot mean squared error
axes(handles.axes2);
plot(cnn.rL);
ac = (1-er)*100; 
assert(er<0.12, 'Too big error');
set(handles.text17, 'String', ac);

